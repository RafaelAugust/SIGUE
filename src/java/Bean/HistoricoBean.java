package Bean;

import javax.faces.bean.ManagedBean;
import javax.faces.bean.SessionScoped;

/**
 *
 * @author Familia
 */
@ManagedBean
@SessionScoped
public class HistoricoBean {
   private int codhistorico;
   private String notaport;
   private String notamat;
   private String notacien;
   private String notahisto;
   private String notageog;

    public int getCodhistorico() {
        return codhistorico;
    }

    public void setCodhistorico(int codhistorico) {
        this.codhistorico = codhistorico;
    }

    public String getNotaport() {
        return notaport;
    }

    public void setNotaport(String notaport) {
        this.notaport = notaport;
    }

    public String getNotamat() {
        return notamat;
    }

    public void setNotamat(String notamat) {
        this.notamat = notamat;
    }

    public String getNotacien() {
        return notacien;
    }

    public void setNotacien(String notacien) {
        this.notacien = notacien;
    }

    public String getNotahisto() {
        return notahisto;
    }

    public void setNotahisto(String notahisto) {
        this.notahisto = notahisto;
    }

    public String getNotageog() {
        return notageog;
    }

    public void setNotageog(String notageog) {
        this.notageog = notageog;
    }
   
   
           
}
